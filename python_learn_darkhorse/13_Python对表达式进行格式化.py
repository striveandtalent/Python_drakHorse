"""
什么是表达式？
一条具有明确执行结果的“代码语句”
eg：1+1 或者 5*2 就是表达式，因为有具体的结果，结果是一个数字
name = "张三"，那么"张三"就是一个表达式
age = 11+11，那么11+11就是一个表达式
因为他们都有具体的结果，只是把具体的结果赋值给了具体的变量
"""
print("2*3的结果是:%s" % (3 * 2))
print(f"2*3的结果是:{2 * 3}")
